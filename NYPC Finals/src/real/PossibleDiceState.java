	public static void setPossibleDiceStateLocation()
	{
		for(int i1=1;i1<=6;i1++)
		{
			for(int i2=1;i2<=6;i2++)
			{
				for(int i3=1;i3<=6;i3++)
				{
					for(int i4=1;i4<=6;i4++)
					{
						for(int i5=1;i5<=6;i5++)
						{
							int[] temp = { i1, i2, i3, i4, i5 };
	                        Arrays.sort(temp);
							for(int j=0;j<possibleDiceState.length;j++)
							{
								if(Arrays.equals(temp, possibleDiceState[j]))
								{
									possibleDiceStateLocation[i1][i2][i3][i4][i5] = j;
									break;
								}
							}
						}
					}
				}
			}
		}
	}
	
   private static void generateCombinations(int start, int depth, int n, int r, int[] result) 
   {
        if (depth == r) 
        {
        	int [] copy = Arrays.copyOf(result, r);
        	possibleTwoDiceState[count] = copy;
        	++count;
            return;
        }

        for (int i = start; i <= n; i++) {
            result[depth] = i;
            generateCombinations(i, depth + 1, n, r, result); // i부터 시작 → 중복 허용 + 오름차순
        }
    }
	
    public static void setPossibleTwoDiceState()
    {
        int n = 6;  // 1~6
        int r = 10; // 주사위 개수
        int[] result = new int[r];
        generateCombinations(1, 0, n, r, result);
    }
    
